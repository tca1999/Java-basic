package Java转义符;

public class ChangeChar {
    public static void main(String[] args) {
//        \t:制表符
        System.out.println("上海\t北京\t苏州");
//        \n:换行符
        System.out.println("上海\n北京");
//        \\:表示一个\
        System.out.println("\\");
//    \":表示一个“
        System.out.println("\"");
//        \':表示一个'
        System.out.println("\'");
//        \r:表示一个回车、
        System.out.println("韩顺平教育\r\n南京");
    }
}
